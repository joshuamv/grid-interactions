
$(document).ready(function() {
  //wait for html

  $(".colorChange").click(function(){
    if( $(this).hasClass("color1") ) {
      $(this).removeClass("color1");
      $(this).addClass("color2");
    }else{
      $(this).addClass("color1");
      $(this).removeClass("color2");
    }
  });
  
});


